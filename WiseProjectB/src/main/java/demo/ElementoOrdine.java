package demo;

public class ElementoOrdine {

    protected String name;
    protected double price;

    public String getNome() {
        return name;
    }

    public void setNome(String value) {
        this.name = value;
    }

    public double getPrezzo() {
        return price;
    }

    public void setPrezzo(double value) {
        this.price = value;
    }

}
